package com.java.model;

/**
 *
 * @author tuong
 */
public class User {

    private String name;
    private String password;
    private String email;
    private int role;
    private int id;

    public User(String name, String password, String email, int role, int id) {
        this.name = name;
        this.password = password;
        this.email = email;
        this.role = role;
        this.id = id;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getRole() {
        return role;
    }

    public void setRole(int role) {
        this.role = role;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public User() {

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
